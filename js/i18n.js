const SUPPORTED_LANGS = ["en", "ms", "zh", "ta"];
const DEFAULT_LANG = "en";

function getCurrentLang() {
  const saved = localStorage.getItem("deenassist-lang");
  return SUPPORTED_LANGS.includes(saved) ? saved : DEFAULT_LANG;
}

function t(key, lang) {
  const dict = TRANSLATIONS[lang || getCurrentLang()] || TRANSLATIONS[DEFAULT_LANG];
  return dict[key] || TRANSLATIONS[DEFAULT_LANG][key] || key;
}

function applyTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n, lang);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder, lang);
  });
  document.documentElement.lang = lang;
}

function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  localStorage.setItem("deenassist-lang", lang);
  applyTranslations(lang);
  document.dispatchEvent(new CustomEvent("deenassist-lang-changed", { detail: { lang } }));
}

function initI18n() {
  applyTranslations(getCurrentLang());
  const selector = document.getElementById("language-select");
  if (selector) {
    selector.value = getCurrentLang();
    selector.addEventListener("change", () => setLanguage(selector.value));
  }
}

window.deenAssistT = t;
window.deenAssistSetLanguage = setLanguage;
window.deenAssistGetLang = getCurrentLang;
window.initI18n = initI18n;
